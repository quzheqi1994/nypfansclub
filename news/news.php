			<h2 class="main-title">中国超薄玻璃惊艳世界：厚度堪比A4纸 轿车撞不碎</h2>
			<div class="author">
				<span class="date">2018年05月09日 21:45</span>
				<a href="#">央视财经</a>
			</div>
			<div class="artical">
				<iframe name="ifd" width="100%" onload="this.height=ifd.document.body.scrollHeight+10" src="/service/videoplayer/" frameborder="0"></iframe>
				<p>原标题：中国造出“超薄玻璃”，厚度堪比A4纸！生产过程全曝光。。。惊艳世界！</p>
				<p>　　<strong>玻璃在日常生活中可以说无处不在，几乎是天天能见到，但您见过厚度只有0.12毫米的玻璃吗？0.12毫米是个什么概念，一张A4打印纸的厚度是0.095毫米，也就是说，这种玻璃只比一张纸略厚一点，更神奇的是，这种玻璃还很结实。</strong></p>
				<p>　　<strong>这种玻璃只比A4纸稍厚 家用轿车150公里时速撞击都毫发无损</strong></p>
				<p>　　中国建材集团蚌埠院功能玻璃研究所副所长曹欣，正带着他的团队做超薄玻璃的落球实验，测试超薄玻璃的性能。</p>
				<div class="img_wrapper"><img src="http://n.sinaimg.cn/news/transform/57/w550h307/20180509/-bEf-haichqz2283825.jpg"></div>
				<p>　　曹欣将55克钢球放在一米的高度，自由落体砸向超薄玻璃。这样的冲击力相当于一辆家用轿车以时速150公里的速度撞停到墙上的冲击力，但玻璃完好无损。</p>
				<p>　　被钢球砸中的这片薄如蝉翼的玻璃2018年4月刚刚下线，<strong>超薄玻璃的厚度只有0.12毫米</strong>，是目前世界上使用浮法工艺批量生产的最薄玻璃。</p>
				<div class="img_wrapper"><img src="http://n.sinaimg.cn/news/transform/56/w550h306/20180509/PGkA-haichqz2283563.jpg"></div>
				<p>　　毫米的玻璃到底有多薄呢？我们现场拿办公用的A4纸进行了对比。两张A4纸的厚度是毫米，比超薄玻璃还厚了0.07毫米。</p>
				<p>　　<strong>中国建材集团蚌埠院功能玻璃研究所副所长 曹欣：</strong>在0.12毫米这个范围上，我相信这是在世界范围内，比较惊艳的一个成果了。</p>
				<p>　　<strong>超薄玻璃也叫超薄电子触控玻璃，是电子信息显示产业的核心材料，用来做手机、电脑、电视显示屏基础材</strong><strong>料</strong>。玻璃越薄，透光性能就会越好，柔韧性好，重量也会随之减轻。但是玻璃太薄又非常易碎，怎样让玻璃既薄又有足够的强度和韧性，是个世界难题。</p>
				<div class="img_wrapper"><img src="http://n.sinaimg.cn/news/transform/59/w550h309/20180509/ZLW8-haichqz2283184.jpg"></div>
				<p>　　<strong>正是因为中国技术的突破和创新，使超薄玻璃的国际市场价格降低了2/3。</strong>然而，曹欣并没有满足0.12毫米的厚度。在0.12毫米玻璃批量下线的时候，他就已经在着手准备0.1毫米超薄玻璃的研发了。</p>
				<div class="img_wrapper"><img src="http://n.sinaimg.cn/news/transform/59/w550h309/20180509/up15-haichqz2282870.jpg"></div>
				<p>　　上世纪五十年代末，英国皮尔金顿玻璃公司向世界宣布玻璃浮法成型工艺研制成功，各国争相抢购专利，但是皮尔金顿却独独对中国进行技术封锁。这样的情况让当时的中国玻璃行业意识到，必须要走一条自主创新的道路。</p>
				<div class="img_wrapper"><img src="http://n.sinaimg.cn/news/transform/54/w550h304/20180509/3OYb-haichqz2282484.jpg"></div>
				<p>　　因为起步较晚，又没有任何经验可以借鉴，当时中国的装备和工艺都跟国外存在一定差距，根本无法做出厚度1.1毫米以下的超薄玻璃。电子信息显示的核心材料仍然受国外垄断，国内电子产品生产商不但价格没法协商，就是供应什么样的材料也要由人家说了算。</p>
				<p>　　为了不再受国外的技术掣肘，蚌埠玻璃工业设计研究院首席科学家彭寿和科研团队经过三十多年的探索，将完全拥有自主知识产权的中国超薄玻璃带上了世界舞台。</p>
				<p>　　2014年6月，突破0.33毫米；</p>
				<p>　　2016年4月突破0.15毫米；</p>
				<p>　　2018年4月突破0.12毫米。</p>
				<div class="img_wrapper"><img src="http://n.sinaimg.cn/news/transform/61/w550h311/20180509/5MfG-haichqz2281560.jpg"></div>
				<p>　　中国制造一次又次刷新了世界纪录。</p>
				<p>　　<strong>蚌埠中建材信息显示材料有限公司总经理 任红灿</strong>：市场转了一圈，现在下游对产品又有了新的要求，现在我拿的就是市场反馈回来的一个样片，我给大家做一个实验。</p>
				<p>　　任红灿掰断的就是用来做手机屏幕的0.33毫米超薄玻璃，是客户跟他们订购的产品。从外观来看，掰断后的玻璃，花纹越多越细，就代表玻璃的强度越高。在同类产品中，这样的强度已经算是较高的了。然而，客户又对任红灿提出了新的要求，想要在这个强度上再增加一个量级。</p>
				<p>　　虽然客户提出的要求可以做到，但是也要通过数百个工艺参数的调整才能完成，开完会的任红灿还要到生产线上看看。</p>
				<p>　　<strong>记者现场探访世界最薄玻璃生产基地 从实验室到大规模量产中国材料让世界惊艳</strong></p>
				<p>　　生产线的环境温度有四五十度，被任红灿称为最不讨喜的厂房，而观测1600度左右炉温的融化情况才是最让人头疼的一件事。</p>
				<div class="img_wrapper"><img src="http://n.sinaimg.cn/news/transform/58/w550h308/20180509/UAdl-haichqz2281182.jpg"></div>
				<p>　　玻璃的性能是否优越，关键在于原料的配比和高温融化时原料产生的化学变化，而炉内的温度有着至关重要的作用，正负2度的温差就会影响玻璃的性能，所以任红灿最关心温度的变化和炉内的融化情况，有丝毫的偏差，整个生产线上的玻璃就要废掉，一天的损失就要达到50万。</p>
				<p>　　炉内的温度将近1600度，里面红彤彤一片，除了刺眼睛，几乎什么都看不见。为了能直观看到里面融化的情况，任红灿拿出来他的观察法宝——镜子。</p>
				<div class="img_wrapper"><img src="http://n.sinaimg.cn/news/transform/59/w550h309/20180509/Fm4a-haichqz2280936.jpg"></div>
				<p>　　<strong>任红灿：</strong>你看是不是像做粥一样冒泡，所以平时观察这个，都用镜子，冒小泡，说明在进行剧烈的化学反应。</p>
				<p>　　透过这个小镜子，可以看到炉内的融化情况，就像煮粥时滚开的气泡。任红灿每次观察特别注意气泡的情况。由于客户提出要增加玻璃的强度，任红灿就需要在制备玻璃的原料中增加一种金属元素。而这种金属元素的熔点要高于其它原料的熔点，所以融化的过程要特别注意，气泡稍微有些异样就要在温度上及时进行调整。</p>
				<div class="img_wrapper"><img src="http://n.sinaimg.cn/news/transform/57/w550h307/20180509/lsm--haichqz2280664.jpg"></div>
				<p>　　<strong>生产超薄玻璃的秘诀，除了高温的窑炉，还有一个关键步骤，锡槽。</strong>窑炉熔化的玻璃溶液流入锡槽装备中，因为锡液的密度较高，玻璃溶液可以浮在锡液表面，再用拉边机将玻璃拉薄，最后进入退火窑进行退火。</p>
				<p>　　<strong>窑炉、锡槽和退火是浮法工艺生产玻璃最为重要的三个环节，每个环节需要调整的参数都非常多。</strong></p>
				<div class="img_wrapper"><img src="http://n.sinaimg.cn/news/transform/58/w550h308/20180509/7NLb-haichqz2280412.jpg"></div>
				<p>　　<strong>任红灿：</strong>这个反复的过程是非常熬人的，对人就是一种折磨，中间可能有时候就是十几个小时，那你就要不停地盯在现场。</p>
				<p>　　坚持与创新，让中国的超薄玻璃在国内国际市场上不断开疆拓土。<strong>2014年以前，任红灿他们的电子信息显示材料在国际上几乎是零销量。而短短四年的时间，在国际市场的份额增长到了40%。</strong></p>
				<div class="img_wrapper"><img src="http://n.sinaimg.cn/news/transform/58/w550h308/20180509/SFGV-haichqz2279999.jpg"></div>
				<p>　　任红灿：我们现在在市场的占有率，应该都是在世界前三。</p>
				<p>　　从实验室到产业化，最难的还是生产线上工艺参数的调整。实验室做出的超薄玻璃样品仅有10厘米宽，而工业生产的超薄玻璃要做到4米宽。这对玻璃强度、韧度的指标有着极大的考验，需要上万次的工艺参数调整。而每种功能不同的玻璃都需要重新调整工艺参数，这样的过程需要持续不断才能保证玻璃的最优性能。</p>
				<p>　　<strong>中国建材集团董事长宋志平：</strong>过去中国在跟跑、并跑，下一步我们要领跑，从中国制造到中国创造。</p>
				<div class="img_wrapper"><img src="http://n.sinaimg.cn/news/transform/55/w550h305/20180509/Fr5t-haichqz2279460.jpg"></div>
				<p>　　薄如蝉翼的玻璃竟然可以经受那么大的冲击力，也难怪曹欣说那是一个令世界惊艳的成果。正是因为有了这些基础材料，中国建材集团建起了属于自己的玻璃王国，发展各种行业所需的高精端玻璃基材。</p>
				<p>　　<strong>半小时观察：竞争从没有终点</strong></p>
				<p>　　从无到有，从不信任到占领市场，越来越多的中国制造正在赢得市场的青睐，这背后是无数人的创新与坚守。</p>
				<p>　　不过，制造业领域的竞争从来没有终点，领先只是暂时的，追赶是常态。</p>
				<p>　　况且，制造业领域的细分产品不胜枚举，在很多领域我们依旧处在跟随状态。而不断追求更完美的产品是中国制造时刻眺望的目标。</p>
				<p>　　我们期待，未来有更多中国的企业，能够走在世界制造业的前列。</p>
	
				<p class="show_author">责任编辑：张建利 </p>
